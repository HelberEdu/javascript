if (10 === 10){ // === compara não só se os valores são iguais, mas também os tipos
    console.log('Mesmo tipo')
}

if (10 != 1){
    console.log('É diferente')
}

if (10 > 1){
    console.log('É maior')
}

if (10 >= 1){
    console.log('É maior ou igual')
}

if (10 <= 10){
    console.log('É maior ou igual')
}

/*
==      Igual
===     Exatamente igual(valor e tipo)
!=      Não é igual
>       Maior
<       Menor
>=      Maior ou igual
<=      Menor ou igual
*/